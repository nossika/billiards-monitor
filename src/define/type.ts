export type Length = number; // 长度
export type Velocity = number; // 速度 vx, vy
export type Mass = number; // 质量 m
export type Radius = number; // 半径 r
export type Color = string; // 颜色，如 #aabbcc
export type Angle = number; // 角度，范围 0 - 360
export type Friction = number; // 摩擦系数，>= 0，越小表示摩擦力越小
export type Elastic = number; // 弹性，范围 0 - 1，越大表示弹性越好
